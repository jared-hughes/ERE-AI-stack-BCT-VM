const subs = [
  ["0000", "0101000"],
  ["0001", "0111000"],
  ["000", "00100"],
  ["0101010100100", "0101001000101"],
  ["0101011100100", "0111001000101"],
  ["0111010100100", "0101001000111"],
  ["0111011100100", "0111001000111"],
  ["010100100", "110100101"],
  ["011100100", "111100101"],
  ["0010101", "0100101"],
  ["0010111", "1100101"],
  ["00101", "00100"],
  ["00100", "0011"],
  ["11010011", "00110"],
  ["11110011", "00111"],
  ["0011", ""],
];
function rot(s) {
  for (let i = 0; i < s.length; i++) {
    for (const [a, b] of subs) {
      if (s.slice(i).startsWith(a)) {
        // replace only does first, left-most replacement.
        return rot(s.replace(a, b));
      }
    }
  }
  return s;
}
console.log(rot("1010100001010000101011"));
